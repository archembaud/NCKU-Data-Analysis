# Demo of integers in Python
a = '2'
b = int(a)
print(f"The values of a and b are {a}, {b}")
print(f"a has type {type(a)} while b has type {type(b)}")

# Demonstrate use of pow(x,y) - power of x to y.
c = pow(b, 2)
print(f'The value of a^2 is {c}, which has type {type(c)}')

# Demonstrate use of abs(b) (absolute value of d)
d = -3
print(f"The abs value of d is {abs(d)}")